/*En JavaScript, los valores se evalúan como verdaderos o falsos cuando se consideran como booleanos.

Los valores que se evalúan como verdaderos se conocen como "truthy".
Los valores que se evalúan como falsos se conocen como "falsy".
Los valores falsy incluyen false, 0, cadenas vacías, null, undefined y NaN. Todos los demás valores se consideran truthy.*/