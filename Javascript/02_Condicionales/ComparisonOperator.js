/*Los operadores de comparación se utilizan para comparar dos valores y devolver true o false dependiendo de la validez de la comparación:

- === igual estricto
- !== no igual estricto
- > mayor que
- >= mayor o igual que
- < menor que
- <= menor o igual que
*/


1 > 3       // false
3 > 1       // true
250 >= 250  // true
1 === 1     // true
1 === 2     // false
1 === '1'   // false