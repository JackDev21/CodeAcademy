/*La declaración if acepta una expresión con un conjunto de paréntesis:

- Si la expresión se evalúa como verdadera, entonces se ejecuta el código dentro de su cuerpo.
- Si la expresión se evalúa como falsa, su cuerpo de código no se ejecutará.*/

const isMailSent = true;

if (isMailSent) {
    console.log('Mail sent to recipient');
}