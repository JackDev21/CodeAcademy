
/*Funciones
Las funciones son uno de los bloques fundamentales en JavaScript. Una función es un conjunto reutilizable de declaraciones para realizar una tarea 
o calcular un valor. Las funciones pueden recibir uno o más valores y pueden devolver un valor al final de su ejecución. Para utilizar una función, 
debes definirla en algún lugar del ámbito donde deseas llamarla.

El código de ejemplo proporcionado contiene una función que recibe 2 valores y devuelve la suma de esos números. */

// Defining the function:
function sum(num1, num2) {
    return num1 + num2;
}

// Calling the function:
sum(3, 6); // 9