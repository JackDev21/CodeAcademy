/*Declaración de Función
Las declaraciones de función se utilizan para crear funciones con nombre. Estas funciones pueden ser llamadas utilizando su nombre declarado. Las declaraciones de función se construyen a partir de:

- La palabra clave `function`.
- El nombre de la función.
- Una lista opcional de parámetros separados por comas encerrados por un conjunto de paréntesis ().
- Un cuerpo de función encerrado en un conjunto de llaves {}.*/


function add(num1, num2) {
    return num1 + num2;
}