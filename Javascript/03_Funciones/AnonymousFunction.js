/*Funciones Anónimas
Las funciones anónimas en JavaScript no tienen una propiedad de nombre. Pueden ser definidas usando la palabra clave `function` o como una función de flecha. 
Mira el ejemplo de código para ver la diferencia entre una función con nombre y una función anónima.*/


// Named function
function rocketToMars() {
    return 'BOOM!';
}

// Anonymous function
const rocketToMars = function () {
    return 'BOOM!';
}